using DevOpsAIAgent.Core.Models;
using DevOpsAIAgent.Core.Interfaces.Services;
using DevOpsAIAgent.Core.Models.Configuration;
using Microsoft.Extensions.Options;
using Microsoft.Extensions.Logging;

namespace DevOpsAIAgent.API.Services.LlmProviders;

/// <summary>
/// OpenRouter LLM provider implementation
/// </summary>
public class OpenRouterProvider : ILlmProvider
{
    private readonly OpenRouterConfiguration _config;
    private readonly ILogger<OpenRouterProvider> _logger;
    private readonly IOpenRouterApi _api;

    public string ProviderName => "OpenRouter";

    public OpenRouterProvider(
        IOptions<LlmConfiguration> options,
        ILogger<OpenRouterProvider> logger,
        IOpenRouterApi api)
    {
        _config = options.Value.OpenRouter;
        _logger = logger;
        _api = api;
    }

    public async Task<AiAnalysis> GenerateDevOpsAnalysisAsync(string prompt, string context, long ciCdEventId = 0, CancellationToken cancellationToken = default)
    {
        try
        {
            var fullPrompt = $@"
            You are a DevOps expert AI. Analyze the following context and provide insights for failure analysis and optimization.

            Context: {context}

            Analysis Request: {prompt}

            Please provide a comprehensive analysis including:
            1. Key findings and root cause analysis
            2. Specific recommendations for improvement
            3. Risk assessment and impact analysis
            4. Prioritized action items with timelines
            5. Prevention strategies for similar issues

            Respond in a structured, clear format that can be easily parsed and displayed in a dashboard.";

            var request = new OpenRouterRequest
            {
                Model = _config.Model,
                Messages = new[]
                {
                    new OpenRouterMessage { Role = "user", Content = fullPrompt }
                },
                Temperature = _config.Temperature,
                MaxTokens = _config.MaxTokens
            };

            var response = await _api.CreateCompletionAsync(
                request,
                $"Bearer {_config.ApiKey}",
                "https://localhost",
                _config.AppName);

            var content = response.Choices?.FirstOrDefault()?.Message?.Content ?? "No analysis generated";
            var tokensUsed = response.Usage?.TotalTokens ?? EstimateTokenCount(fullPrompt + content);

            return new AiAnalysis
            {
                // Id is auto-generated by database
                CiCdEventId = ciCdEventId,
                AnalysisText = content,
                GitDiff = ExtractGitDiffFromContext(context),
                ErrorLog = ExtractErrorLogFromContext(context),
                ModelUsed = $"{ProviderName}:{_config.Model}",
                TokensUsed = tokensUsed,
                CreatedAt = DateTime.UtcNow
            };
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "Error generating DevOps analysis with OpenRouter: {Error}", ex.Message);
            throw new InvalidOperationException($"OpenRouter analysis failed: {ex.Message}", ex);
        }
    }

