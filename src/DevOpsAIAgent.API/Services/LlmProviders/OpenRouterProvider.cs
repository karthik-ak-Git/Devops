                MaxTokens = _config.MaxTokens
            };

            var response = await _api.CreateCompletionAsync(
                request,
                $"Bearer {_config.ApiKey}",
                "https://localhost",
                _config.AppName);

            return response.Choices?.FirstOrDefault()?.Message?.Content ?? "No code review generated";
        }
        catch (Exception ex)
        {
            _logger.LogError(ex, "Error generating code review with OpenRouter");
            throw;
        }
    }

